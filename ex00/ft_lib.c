int	ft_sqrt(int nb)
{
	int i;

	i = 0;
	if (!(nb > 2147395600))
	while (++i <= nb)
		if (i * i == nb && nb % i == 0)
		{
			g_RC = i;
			return (1);
		}
	return 0;
}
